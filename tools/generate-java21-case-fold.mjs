import { Buffer } from 'node:buffer';
import { createHash } from 'node:crypto';
import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const UNICODE_VERSION = '15.0.0';
const SOURCE_URL = `https://www.unicode.org/Public/${UNICODE_VERSION}/ucd/UnicodeData.txt`;
const SOURCE_SHA256 = '806e9aed65037197f1ec85e12be6e8cd870fc5608b4de0fffd990f689f376a73';
const OUTPUT_PATH = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  '../src/ui/data/java21-simple-case-fold.ts',
);

function sha256(value) {
  return createHash('sha256').update(value).digest('hex');
}

async function loadSource() {
  const sourceArgument = process.argv.find((argument) => argument.startsWith('--source='));
  if (sourceArgument) return readFile(sourceArgument.slice('--source='.length));

  const response = await globalThis.fetch(SOURCE_URL);
  if (!response.ok) {
    throw new Error(`Unable to download ${SOURCE_URL}: ${response.status} ${response.statusText}`);
  }
  return Buffer.from(await response.arrayBuffer());
}

function createFoldData(source) {
  const rows = new Map();
  for (const line of source.toString('utf8').split(/\r?\n/u)) {
    if (!line) continue;
    const fields = line.split(';');
    const codePoint = Number.parseInt(fields[0], 16);
    rows.set(codePoint, {
      upper: fields[12] ? Number.parseInt(fields[12], 16) : codePoint,
      lower: fields[13] ? Number.parseInt(fields[13], 16) : codePoint,
    });
  }

  const data = [];
  for (let codePoint = 0; codePoint <= 0x10ffff; codePoint += 1) {
    const upper = rows.get(codePoint)?.upper ?? codePoint;
    const folded = rows.get(upper)?.lower ?? upper;
    if (folded !== codePoint) data.push(codePoint, folded);
  }
  return data;
}

function render(data) {
  const values = data.map((value) => `0x${value.toString(16)}`);
  const lines = [];
  for (let index = 0; index < values.length; index += 8) {
    lines.push(`  ${values.slice(index, index + 8).join(', ')},`);
  }
  const dataSha256 = sha256(data.join(','));

  return `/*
 * Generated by node tools/generate-java21-case-fold.mjs.
 * Source: ${SOURCE_URL}
 * Source SHA-256: ${SOURCE_SHA256}
 * Algorithm: UnicodeData simple lowercase(simple uppercase(code point)).
 * Do not edit by hand.
 */

export const JAVA21_CASE_FOLD_UNICODE_VERSION = '${UNICODE_VERSION}';
export const JAVA21_CASE_FOLD_SOURCE_URL = '${SOURCE_URL}';
export const JAVA21_CASE_FOLD_SOURCE_SHA256 = '${SOURCE_SHA256}';
export const JAVA21_CASE_FOLD_DATA_SHA256 = '${dataSha256}';

/** Flat source/target pairs, sorted by source code point. */
export const JAVA21_SIMPLE_CASE_FOLD_DATA: readonly number[] = [
${lines.join('\n')}
];
`;
}

const source = await loadSource();
const actualSourceHash = sha256(source);
if (actualSourceHash !== SOURCE_SHA256) {
  throw new Error(`UnicodeData.txt SHA-256 mismatch: expected ${SOURCE_SHA256}, got ${actualSourceHash}`);
}

const output = render(createFoldData(source));
if (process.argv.includes('--check')) {
  const existing = await readFile(OUTPUT_PATH, 'utf8');
  if (existing !== output) {
    throw new Error(`${path.relative(process.cwd(), OUTPUT_PATH)} is stale; run this generator.`);
  }
  console.log(`Verified ${path.relative(process.cwd(), OUTPUT_PATH)} (${sha256(output)}).`);
} else {
  await writeFile(OUTPUT_PATH, output, 'utf8');
  console.log(`Wrote ${path.relative(process.cwd(), OUTPUT_PATH)}.`);
}
